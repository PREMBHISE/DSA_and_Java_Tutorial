public class variableExample{
    public static void main(String[] args){

        int age = 25;   // Umra - pura number hai
        byte level = 5;                // Game ka level - chhota number
        short temperature = 300;         // Temperature - thoda bada number
        long population = 789456123L;   // Population - bahut bada number (L lagana jaruri hai)

        //floating point types
        float height = 5.9f;    // Height feet mein - decimal ke sath
        double weight = 72.5;   // Weight kg mein - zyada accurate decimal

        //charachter type
        char grade = 'A';    // Grade - ek character

        //Boolean type
        boolean isStudent = true; // Haan ya na - true ya false

        //String type
        String name = "Prem Bhise";     // Naam - text format mein

        System.out.println("Name: " + name);
        System.out.println("Age: " + age);
        System.out.println("Level: " + level);
        System.out.println("Temperature: " + temperature);
        System.out.println("Population: " + population);
        System.out.println("Height: " + height);
        System.out.println("Weight: " + weight);
        System.out.println("Grade: " + grade);
        System.out.println("Is Student: " + isStudent);
    }
}